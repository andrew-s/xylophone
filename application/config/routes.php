/*---------------------------------------------------------------
| URI ROUTING
|----------------------------------------------------------------
| This file lets you re-map URI requests to specific controller functions.
|
| Typically there is a one-to-one relationship between a URL string
| and its corresponding controller class/method. The segments in a
| URL normally follow this pattern:
|
|    example.com/class/method/id/
|
| In some instances, however, you may want to remap this relationship
| so that a different class/function is called than the one
| corresponding to the URL.
|
| Please see the user guide for complete details:
|
|    http://xylophone.io/user_guide/general/routing.html
|
|----------------------------------------------------------------
| RESERVED ROUTES
|----------------------------------------------------------------
|
| There are six reserved routes:
|
|    $route['default_controller'] = 'welcome';
|
| This route indicates which controller class should be loaded if the
| URI contains no data. In the above example, the "welcome" class
| would be loaded.
|
|    $route['error_404_override']       = 'errors/page_missing';
|    $route['error_db_override']        = 'errors/database';
|    $route['error_general_override']   = 'errors/non_fatal';
|    $route['error_php_override']       = 'errors/fatal';
|
| These routes tell the Router which controller/method to use instead
| of the basic error templates found in the views folder.
|
|    $route['translate_uri_dashes'] = FALSE;
|
| This is not exactly a route, but allows you to automatically route
| controller and method names that contain dashes. '-' isn't a valid
| class or method name character, so it requires translation.
| When you set this option to TRUE, it will replace ALL dashes in the
| controller and method URI segments.
|
| Examples:
|   my-controller/index     -> my_controller/index
|   my-controller/my-method -> my_controller/my_method
*/
$route['default_controller'] = 'welcome';
$route['error_404_override'] = '';
$route['error_db_override'] = '';
$route['error_general_override'] = '';
$route['error_php_override'] = '';
$route['translate_uri_dashes'] = false;

